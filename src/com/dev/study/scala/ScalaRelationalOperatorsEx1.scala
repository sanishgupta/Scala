package com.dev.study.scala

object ScalaRelationalOperatorsEx1 {
  def main(args: Array[String]) {
    var a = 10;
    var b  = 20;

    println("a == b = " + (a == b));
    println("a != b = " + (a != b));
    println("a > b = "  + (a > b));
    println("a < b = "  + (a < b));
    println("b >= a = " + (b >= a));
    println("b <= a = " + (b <= a));
  }
}